#ifndef LLVM_LIB_TARGET_RV16K_INSTPRINTER_RV16KINSTPRINTER_H
#define LLVM_LIB_TARGET_RV16K_INSTPRINTER_RV16KINSTPRINTER_H

#include "MCTargetDesc/RV16KMCTargetDesc.h"
#include "llvm/MC/MCInstPrinter.h"

namespace llvm {
class MCOperand;

class RV16KInstPrinter : public MCInstPrinter {
public:
  RV16KInstPrinter(const MCAsmInfo &MAI, const MCInstrInfo &MII,
                   const MCRegisterInfo &MRI)
      : MCInstPrinter(MAI, MII, MRI) {}

  void printInst(const MCInst *MI, raw_ostream &O, StringRef Annot,
                 const MCSubtargetInfo &STI) override;
  void printRegName(raw_ostream &O, unsigned RegNo) const override;

  void printOperand(const MCInst *MI, unsigned OpNo, raw_ostream &O,
                    const char *Modifier = nullptr);

  // Autogenerated by tblgen.
  void printInstruction(const MCInst *MI, raw_ostream &O);
  static const char *getRegisterName(unsigned RegNo,
                                     unsigned AltIdx = RV16K::ABIRegAltName);
};
} // namespace llvm

#endif
